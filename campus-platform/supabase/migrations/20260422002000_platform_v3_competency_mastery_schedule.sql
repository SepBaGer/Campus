    legacy.content_block_id as source_content_block_id
  from tmp_mastery_state_legacy as legacy
  join catalog.content_block_competency as primary_map
    on primary_map.content_block_id = legacy.content_block_id
   and primary_map.is_primary
  order by
    legacy.person_id,
