create or replace view public.platform_portal_snapshot_v
with (security_invoker = true) as
with current_enrollment as (
  select
    enrollment_row.person_id,
    enrollment_row.id as enrollment_id,
    course_run.id as course_run_id,
    course_run.title as active_run_label,
    course.id as course_id,
    course.title as course_title
  from enrollment.enrollment as enrollment_row
  join delivery.course_run as course_run
    on course_run.id = enrollment_row.course_run_id
  join catalog.course as course
    on course.id = course_run.course_id
  where enrollment_row.person_id = (select auth.uid())
    and enrollment_row.status in ('active', 'completed')
  order by enrollment_row.updated_at desc, enrollment_row.created_at desc
  limit 1
),
course_progress as (
  select
    current_enrollment.person_id,
    count(block.id)::integer as total_blocks,
    count(distinct attempt.content_block_id)::integer as completed_blocks
  from current_enrollment
  join catalog.content_block as block
    on block.course_id = current_enrollment.course_id
  left join learning.attempt as attempt
    on attempt.person_id = current_enrollment.person_id
   and attempt.content_block_id = block.id
   and attempt.status = 'completed'
  group by current_enrollment.person_id
),
review_queue as (
  select
    schedule.person_id,
    count(*) filter (where schedule.next_review_at is not null and schedule.next_review_at < now())::integer as due_reviews_count,
    min(schedule.next_review_at) filter (where schedule.next_review_at is not null) as next_review_at
  from learning.spaced_schedule as schedule
  where schedule.person_id = (select auth.uid())
  group by schedule.person_id
)
select
  person.full_name as learner_name,
  case
    when exists (
      select 1
      from enrollment.entitlement as entitlement
      where entitlement.person_id = person.id
        and entitlement.code = 'premium-membership'
        and entitlement.status = 'active'
    ) then 'Premium entitlement'
    else 'Free entitlement'
  end as membership_label,
  current_enrollment.course_title as enrolled_course_title,
  current_enrollment.active_run_label,
  coalesce(course_progress.completed_blocks, 0) as completed_blocks,
  coalesce(course_progress.total_blocks, 0) as total_blocks,
  case
    when coalesce(course_progress.total_blocks, 0) = 0 then 0
    else floor((course_progress.completed_blocks::numeric / course_progress.total_blocks::numeric) * 100)::integer
  end as progress_percent,
  review_queue.next_review_at,
  coalesce(review_queue.due_reviews_count, 0) as due_reviews_count,
  case
    when coalesce(review_queue.due_reviews_count, 0) > 0 then 'Atencion requerida'
    else 'Ritmo saludable'
  end as at_risk_label
from current_enrollment
join identity.person as person
  on person.id = current_enrollment.person_id
left join course_progress
  on course_progress.person_id = person.id
left join review_queue
  on review_queue.person_id = person.id;

create or replace view public.platform_portal_mastery_v
with (security_invoker = true) as
with current_enrollment as (
  select
    enrollment_row.person_id,
    course.id as course_id,
    course.title as course_title
  from enrollment.enrollment as enrollment_row
  join delivery.course_run as course_run
    on course_run.id = enrollment_row.course_run_id
  join catalog.course as course
    on course.id = course_run.course_id
  where enrollment_row.person_id = (select auth.uid())
    and enrollment_row.status in ('active', 'completed')
  order by enrollment_row.updated_at desc, enrollment_row.created_at desc
  limit 1
)
select
  current_enrollment.course_title,
  competency.slug as competency_slug,
  competency.title as competency_title,
  competency.bloom_level,
  course_competency.position,
  coalesce(mastery.level, 0.00) as mastery_level,
  mastery.last_reviewed_at,
  coalesce(schedule.next_review_at, mastery.next_review_at) as next_review_at,
  coalesce(mastery.repetitions, 0) as repetitions,
  coalesce(mastery.interval_days, 0) as interval_days,
  coalesce(mastery.ease_factor, 2.50) as ease_factor,
  coalesce(schedule.next_review_at, mastery.next_review_at) is not null
    and coalesce(schedule.next_review_at, mastery.next_review_at) < now() as is_due
from current_enrollment
join catalog.course_competency as course_competency
  on course_competency.course_id = current_enrollment.course_id
join catalog.competency as competency
  on competency.id = course_competency.competency_id
left join learning.mastery_state as mastery
  on mastery.person_id = current_enrollment.person_id
 and mastery.competency_id = competency.id
left join learning.spaced_schedule as schedule
  on schedule.person_id = current_enrollment.person_id
 and schedule.competency_id = competency.id
order by course_competency.position;

