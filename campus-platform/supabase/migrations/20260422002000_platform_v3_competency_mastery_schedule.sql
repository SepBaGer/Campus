with competency_counts as (
  select
    course_competency.course_id,
    count(*)::integer as competency_count
  from catalog.course_competency
  group by course_competency.course_id
),
competency_buckets as (
  select
    course_competency.course_id,
    course_competency.competency_id,
    row_number() over (
      partition by course_competency.course_id
      order by course_competency.position, course_competency.competency_id
    ) as competency_bucket
  from catalog.course_competency
),
block_buckets as (
  select
    block.id as content_block_id,
    block.course_id,
    ntile(competency_counts.competency_count) over (
      partition by block.course_id
      order by block.position, block.id
    ) as competency_bucket
  from catalog.content_block as block
  join competency_counts
    on competency_counts.course_id = block.course_id
)
insert into catalog.content_block_competency (
  content_block_id,
  competency_id,
  position,
  weight,
  is_primary
)
select
  block_buckets.content_block_id,
  competency_buckets.competency_id,
  1,
  1.0000,
  true
from block_buckets
join competency_buckets
  on competency_buckets.course_id = block_buckets.course_id
 and competency_buckets.competency_bucket = block_buckets.competency_bucket
on conflict (content_block_id, competency_id) do update
  set position = excluded.position,
      weight = excluded.weight,
      is_primary = excluded.is_primary,
      updated_at = now();

