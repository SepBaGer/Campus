with primary_map as (
  select
    mapping.content_block_id,
    mapping.competency_id
  from catalog.content_block_competency as mapping
  where mapping.is_primary
),
totals as (
  select
    primary_map.competency_id,
    count(*)::numeric as total_blocks
  from primary_map
  group by primary_map.competency_id
),
completed as (
  select
    attempt.person_id,
    primary_map.competency_id,
    count(distinct attempt.content_block_id)::numeric as completed_blocks
  from learning.attempt as attempt
  join primary_map
    on primary_map.content_block_id = attempt.content_block_id
  where attempt.status = 'completed'
  group by attempt.person_id, primary_map.competency_id
),
latest_schedule as (
  select distinct on (legacy.person_id, primary_map.competency_id)
    legacy.person_id,
    primary_map.competency_id,
    legacy.content_block_id as source_content_block_id,
    legacy.repetitions,
    legacy.interval_days,
    legacy.ease_factor,
    legacy.last_reviewed_at,
    legacy.next_review_at,
    legacy.created_at,
    legacy.updated_at
  from tmp_mastery_state_legacy as legacy
  join primary_map
    on primary_map.content_block_id = legacy.content_block_id
  order by
    legacy.person_id,
    primary_map.competency_id,
    legacy.last_reviewed_at desc nulls last,
    legacy.updated_at desc nulls last,
    legacy.content_block_id desc
),
person_competencies as (
  select completed.person_id, completed.competency_id
  from completed
  union
  select latest_schedule.person_id, latest_schedule.competency_id
  from latest_schedule
)
insert into learning.mastery_state (
  person_id,
  competency_id,
  level,
  repetitions,
  interval_days,
  ease_factor,
  last_reviewed_at,
  next_review_at,
  created_at,
  updated_at
)
select
  person_competencies.person_id,
  person_competencies.competency_id,
  coalesce(round(completed.completed_blocks / nullif(totals.total_blocks, 0), 2), 0.00),
  coalesce(
    latest_schedule.repetitions,
    case
      when coalesce(completed.completed_blocks, 0) > 0 then 1
      else 0
    end
  ),
  coalesce(
    latest_schedule.interval_days,
    case
      when coalesce(completed.completed_blocks, 0) > 0 then 3
      else 0
    end
  ),
  coalesce(latest_schedule.ease_factor, 2.50),
  latest_schedule.last_reviewed_at,
  latest_schedule.next_review_at,
  coalesce(latest_schedule.created_at, now()),
  now()
from person_competencies
left join totals
  on totals.competency_id = person_competencies.competency_id
left join completed
  on completed.person_id = person_competencies.person_id
 and completed.competency_id = person_competencies.competency_id
left join latest_schedule
  on latest_schedule.person_id = person_competencies.person_id
 and latest_schedule.competency_id = person_competencies.competency_id;

