create or replace view learning.v_student_risk as
select
  schedule.person_id,
  count(*) filter (where schedule.next_review_at is not null and schedule.next_review_at < now())::integer as overdue_reviews,
  min(schedule.next_review_at) filter (where schedule.next_review_at is not null) as next_review_at,
  case
    when count(*) filter (where schedule.next_review_at is not null and schedule.next_review_at < now()) > 0 then 'at-risk'
    else 'healthy'
  end as risk_label
from learning.spaced_schedule as schedule
group by schedule.person_id;

