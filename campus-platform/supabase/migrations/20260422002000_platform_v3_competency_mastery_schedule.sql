  person_id,
  competency_id,
  source_content_block_id,
  next_review_at,
  stability,
  difficulty,
  last_reviewed_at,
  created_at,
  updated_at
)
select
  mastery.person_id,
  mastery.competency_id,
  latest_sources.source_content_block_id,
  coalesce(mastery.next_review_at, coalesce(mastery.last_reviewed_at, now()) + interval '3 days'),
  greatest(mastery.interval_days::numeric, 1),
  greatest(round(5 - mastery.ease_factor, 2), 0::numeric),
  mastery.last_reviewed_at,
  mastery.created_at,
  now()
from learning.mastery_state as mastery
left join latest_sources
  on latest_sources.person_id = mastery.person_id
 and latest_sources.competency_id = mastery.competency_id
on conflict (person_id, competency_id) do update
  set source_content_block_id = excluded.source_content_block_id,
      next_review_at = excluded.next_review_at,
      stability = excluded.stability,
      difficulty = excluded.difficulty,
      last_reviewed_at = excluded.last_reviewed_at,
      updated_at = now();

create or replace function private.sync_learning_attempt_from_legacy_progress()
returns trigger
language plpgsql
security definer
set search_path = ''
as $$
declare
  v_content_block_id bigint;
  v_course_id bigint;
  v_competency_id bigint;
  v_completed_blocks integer;
  v_total_blocks integer;
  v_next_review_at timestamptz;
begin
  select
    block.id,
    block.course_id
    into v_content_block_id, v_course_id
  from catalog.content_block as block
  where block.legacy_lesson_id = new.lesson_id
  limit 1;

  if v_content_block_id is null then
    return new;
  end if;

  select
    mapping.competency_id
    into v_competency_id
  from catalog.content_block_competency as mapping
  where mapping.content_block_id = v_content_block_id
    and mapping.is_primary
  order by mapping.position
  limit 1;

  if v_competency_id is null then
    select
      course_competency.competency_id
      into v_competency_id
    from catalog.course_competency as course_competency
    where course_competency.course_id = v_course_id
    order by course_competency.position
    limit 1;
  end if;

  insert into learning.attempt (
    person_id,
    content_block_id,
    status,
    completed_at,
    xp_earned,
    legacy_progress_id,
    created_at,
    updated_at
  )
  values (
    new.user_id,
    v_content_block_id,
    'completed',
    new.completed_at,
    new.xp_earned,
    new.id,
    new.completed_at,
    now()
  )
  on conflict (person_id, content_block_id) do update
    set status = excluded.status,
        completed_at = excluded.completed_at,
        xp_earned = excluded.xp_earned,
        updated_at = now();

  if v_competency_id is null then
    perform private.ensure_pilot_enrollment(new.user_id);
    return new;
  end if;

  select
    count(*)::integer
    into v_total_blocks
  from catalog.content_block_competency as mapping
  where mapping.competency_id = v_competency_id
    and mapping.is_primary;

  select
    count(distinct attempt.content_block_id)::integer
    into v_completed_blocks
  from learning.attempt as attempt
  join catalog.content_block_competency as mapping
    on mapping.content_block_id = attempt.content_block_id
   and mapping.is_primary
  where attempt.person_id = new.user_id
    and mapping.competency_id = v_competency_id
    and attempt.status = 'completed';

  v_next_review_at := new.completed_at + interval '3 days';

  insert into learning.mastery_state (
    person_id,
    competency_id,
    level,
    repetitions,
    interval_days,
    ease_factor,
    last_reviewed_at,
    next_review_at,
    created_at,
    updated_at
  )
  values (
    new.user_id,
    v_competency_id,
    coalesce(round(v_completed_blocks::numeric / nullif(v_total_blocks, 0), 2), 0.00),
    1,
    3,
    2.50,
    new.completed_at,
    v_next_review_at,
    new.completed_at,
    now()
  )
  on conflict (person_id, competency_id) do update
    set level = greatest(learning.mastery_state.level, excluded.level),
        repetitions = greatest(learning.mastery_state.repetitions, excluded.repetitions),
        interval_days = excluded.interval_days,
        ease_factor = excluded.ease_factor,
        last_reviewed_at = excluded.last_reviewed_at,
        next_review_at = excluded.next_review_at,
        updated_at = now();

  insert into learning.spaced_schedule (
    person_id,
    competency_id,
    source_content_block_id,
    next_review_at,
    stability,
    difficulty,
    last_reviewed_at,
    created_at,
    updated_at
  )
  values (
    new.user_id,
    v_competency_id,
    v_content_block_id,
    v_next_review_at,
    3,
    2.50,
    new.completed_at,
    new.completed_at,
    now()
  )
  on conflict (person_id, competency_id) do update
    set source_content_block_id = excluded.source_content_block_id,
        next_review_at = excluded.next_review_at,
        stability = excluded.stability,
        difficulty = excluded.difficulty,
        last_reviewed_at = excluded.last_reviewed_at,
        updated_at = now();

  perform private.ensure_pilot_enrollment(new.user_id);

  return new;
end;
$$;

