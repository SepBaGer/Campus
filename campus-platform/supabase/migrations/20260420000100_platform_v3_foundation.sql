create or replace view public.platform_portal_snapshot_v
with (security_invoker = true) as
with current_enrollment as (
  select
    enrollment_row.person_id,
    enrollment_row.id as enrollment_id,
    course_run.id as course_run_id,
    course_run.title as active_run_label,
    course.id as course_id,
    course.title as course_title
  from enrollment.enrollment as enrollment_row
  join delivery.course_run as course_run
    on course_run.id = enrollment_row.course_run_id
  join catalog.course as course
    on course.id = course_run.course_id
  where enrollment_row.person_id = (select auth.uid())
    and enrollment_row.status in ('active', 'completed')
  order by enrollment_row.updated_at desc, enrollment_row.created_at desc
  limit 1
)
select
  person.full_name as learner_name,
  case
    when exists (
      select 1
      from enrollment.entitlement as entitlement
      where entitlement.person_id = person.id
        and entitlement.code = 'premium-membership'
        and entitlement.status = 'active'
    ) then 'Premium entitlement'
    else 'Free entitlement'
  end as membership_label,
  current_enrollment.course_title as enrolled_course_title,
  current_enrollment.active_run_label,
  count(attempt.id)::integer as completed_blocks,
  count(block.id)::integer as total_blocks,
  case
    when count(block.id) = 0 then 0
    else floor((count(attempt.id)::numeric / count(block.id)::numeric) * 100)::integer
  end as progress_percent,
  min(mastery.next_review_at) filter (where mastery.next_review_at is not null) as next_review_at,
  case
    when count(*) filter (where mastery.next_review_at is not null and mastery.next_review_at < now()) > 0 then 'Atencion requerida'
    else 'Ritmo saludable'
  end as at_risk_label
from current_enrollment
join identity.person as person
  on person.id = current_enrollment.person_id
join catalog.content_block as block
  on block.course_id = current_enrollment.course_id
left join learning.attempt as attempt
  on attempt.person_id = person.id
 and attempt.content_block_id = block.id
 and attempt.status = 'completed'
left join learning.mastery_state as mastery
  on mastery.person_id = person.id
 and mastery.content_block_id = block.id
group by
  person.id,
  person.full_name,
  current_enrollment.course_title,
  current_enrollment.active_run_label;

