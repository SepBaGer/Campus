create or replace function private.ensure_pilot_enrollment(p_person_id uuid)
returns void
language plpgsql
security definer
set search_path = ''
as $$
declare
  v_run_id bigint;
begin
  select course_run.id
    into v_run_id
  from delivery.course_run as course_run
  where course_run.slug = 'power-skills-pilot-open'
  limit 1;

  if v_run_id is null then
    return;
  end if;

  insert into enrollment.enrollment (person_id, course_run_id, status, source)
  values (p_person_id, v_run_id, 'active', 'legacy-profile-sync')
  on conflict (person_id, course_run_id) do update
    set status = excluded.status,
        source = excluded.source,
        updated_at = now();
end;
$$;

create or replace function private.upsert_default_entitlements(
  p_person_id uuid,
  p_membership_status text,
  p_membership_plan text,
  p_membership_expires_at timestamptz
)
returns void
language plpgsql
security definer
set search_path = ''
as $$
begin
  insert into enrollment.entitlement (
    person_id,
    code,
    status,
    source,
    starts_at,
    metadata
  )
  values (
    p_person_id,
    'free-access',
    'active',
    'legacy-profile-sync',
    now(),
    jsonb_build_object('plan', coalesce(p_membership_plan, 'free'))
  )
  on conflict (person_id, code) do update
    set status = 'active',
        metadata = excluded.metadata,
        updated_at = now();

  if coalesce(p_membership_plan, 'free') <> 'free'
     or coalesce(p_membership_status, 'inactive') <> 'inactive' then
    insert into enrollment.entitlement (
      person_id,
      code,
      status,
      source,
      starts_at,
      ends_at,
      metadata
    )
    values (
      p_person_id,
      'premium-membership',
      case
        when coalesce(p_membership_status, 'inactive') = 'active' then 'active'
        when coalesce(p_membership_status, 'inactive') = 'past_due' then 'past_due'
        when coalesce(p_membership_status, 'inactive') = 'cancelled' then 'cancelled'
        else 'inactive'
      end,
      'legacy-profile-sync',
      now(),
      p_membership_expires_at,
      jsonb_build_object('plan', coalesce(p_membership_plan, 'premium'))
    )
    on conflict (person_id, code) do update
      set status = excluded.status,
          ends_at = excluded.ends_at,
          metadata = excluded.metadata,
          updated_at = now();
  else
    delete from enrollment.entitlement
    where person_id = p_person_id
      and code = 'premium-membership';
  end if;

  if exists (
    select 1
    from public.progress
    where user_id = p_person_id
  ) or coalesce(p_membership_status, 'inactive') = 'active' then
    perform private.ensure_pilot_enrollment(p_person_id);
  end if;
end;
$$;

create or replace function private.sync_identity_person_from_auth_user()
returns trigger
language plpgsql
security definer
set search_path = ''
as $$
begin
  insert into identity.person (
    id,
    email,
    full_name,
    source,
    created_at,
    updated_at
  )
  values (
    new.id,
    new.email,
    coalesce(new.raw_user_meta_data ->> 'display_name', split_part(coalesce(new.email, ''), '@', 1), ''),
    'auth-user-trigger',
    now(),
    now()
  )
  on conflict (id) do update
    set email = excluded.email,
        full_name = case
          when excluded.full_name = '' then identity.person.full_name
          else excluded.full_name
        end,
        updated_at = now();

  insert into identity.person_role (person_id, role_code, source)
  values (new.id, 'student', 'auth-user-trigger')
  on conflict (person_id, role_code) do nothing;

  perform private.upsert_default_entitlements(new.id, 'inactive', 'free', null);

  return new;
end;
$$;

create or replace function private.sync_identity_person_from_legacy_profile()
returns trigger
language plpgsql
security definer
set search_path = ''
as $$
declare
  v_email text;
begin
  select users.email
    into v_email
  from auth.users as users
  where users.id = new.id;

  insert into identity.person (
    id,
    email,
    full_name,
    avatar_url,
    source,
    created_at,
    updated_at
  )
  values (
    new.id,
    v_email,
    coalesce(nullif(new.display_name, ''), split_part(coalesce(v_email, ''), '@', 1), ''),
    new.avatar_url,
    'legacy-profile-trigger',
    coalesce(new.created_at, now()),
    now()
  )
  on conflict (id) do update
    set email = coalesce(excluded.email, identity.person.email),
        full_name = coalesce(nullif(excluded.full_name, ''), identity.person.full_name),
        avatar_url = excluded.avatar_url,
        updated_at = now();

  insert into identity.person_role (person_id, role_code, source)
  values (new.id, 'student', 'legacy-profile-trigger')
  on conflict (person_id, role_code) do nothing;

  perform private.upsert_default_entitlements(
    new.id,
    new.membership_status,
    new.membership_plan,
    new.membership_expires_at
  );

  return new;
end;
$$;

create or replace function private.sync_learning_attempt_from_legacy_progress()
returns trigger
language plpgsql
security definer
set search_path = ''
as $$
declare
  v_content_block_id bigint;
begin
  select block.id
    into v_content_block_id
  from catalog.content_block as block
  where block.legacy_lesson_id = new.lesson_id
  limit 1;

  if v_content_block_id is null then
    return new;
  end if;

  insert into learning.attempt (
    person_id,
    content_block_id,
    status,
    completed_at,
    xp_earned,
    legacy_progress_id,
    created_at,
    updated_at
  )
  values (
    new.user_id,
