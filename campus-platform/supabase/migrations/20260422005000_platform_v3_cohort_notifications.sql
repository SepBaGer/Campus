insert into identity.person_notification_preference (
  person_id,
  channel_code,
  is_enabled
)
select
  person.id,
  'email',
  exists (
    select 1
    from enrollment.enrollment as enrollment_row
    where enrollment_row.person_id = person.id
      and enrollment_row.status in ('active', 'completed')
  )
from identity.person as person
on conflict (person_id, channel_code) do nothing;

insert into identity.person_notification_preference (
  person_id,
  channel_code,
  is_enabled
)
select
  person.id,
  'web',
  false
from identity.person as person
on conflict (person_id, channel_code) do nothing;

