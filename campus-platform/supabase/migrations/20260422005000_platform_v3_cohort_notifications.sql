-- platform v3 cohort notifications
-- Introduce notification preferences, web push subscriptions, notification templates
-- and dispatch logs to support sequenced cohort comms from the active campus-platform surface.

create table if not exists identity.person_notification_preference (
  person_id uuid not null references identity.person(id) on delete cascade,
  channel_code text not null check (channel_code in ('email', 'web')),
  is_enabled boolean not null default false,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  primary key (person_id, channel_code)
);

create table if not exists identity.web_push_subscription (
  id bigint generated by default as identity primary key,
  person_id uuid not null references identity.person(id) on delete cascade,
  endpoint text not null unique,
  p256dh_key text not null,
  auth_key text not null,
  user_agent text,
  is_active boolean not null default true,
  last_seen_at timestamptz not null default now(),
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists delivery.notification_template (
  id bigint generated by default as identity primary key,
  course_run_id bigint not null references delivery.course_run(id) on delete cascade,
  slug text not null,
  title text not null,
  channel_code text not null check (channel_code in ('email', 'web')),
  audience_code text not null default 'active'
    check (audience_code in ('all', 'invited', 'active', 'completed')),
  trigger_code text not null default 'manual'
    check (trigger_code in ('manual', 'before_run_start', 'after_run_start', 'after_run_end')),
  offset_days integer not null default 0,
  offset_hours integer not null default 0,
  subject_template text not null default '',
  body_template text not null default '',
  cta_label text,
  cta_url text,
  status text not null default 'draft' check (status in ('draft', 'active', 'archived')),
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  unique (course_run_id, slug)
);

create table if not exists delivery.notification_dispatch (
  id bigint generated by default as identity primary key,
  template_id bigint not null references delivery.notification_template(id) on delete cascade,
  course_run_id bigint not null references delivery.course_run(id) on delete cascade,
  person_id uuid not null references identity.person(id) on delete cascade,
  channel_code text not null check (channel_code in ('email', 'web')),
  scheduled_for timestamptz not null,
  status text not null default 'pending' check (status in ('pending', 'sent', 'skipped', 'failed')),
  rendered_subject text,
  rendered_body text not null,
  cta_label text,
  cta_url text,
  provider_message_id text,
  error_message text,
  sent_at timestamptz,
  metadata jsonb not null default '{}'::jsonb,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  unique (template_id, person_id, channel_code, scheduled_for)
);

create index if not exists idx_identity_notification_preference_person
  on identity.person_notification_preference(person_id, channel_code);
create index if not exists idx_identity_web_push_subscription_person
  on identity.web_push_subscription(person_id, is_active, last_seen_at desc);
create index if not exists idx_delivery_notification_template_run_status
  on delivery.notification_template(course_run_id, status, trigger_code, channel_code);
create index if not exists idx_delivery_notification_dispatch_run_status
  on delivery.notification_dispatch(course_run_id, status, scheduled_for);
create index if not exists idx_delivery_notification_dispatch_person
  on delivery.notification_dispatch(person_id, channel_code, created_at desc);

grant usage, select on all sequences in schema identity to authenticated;
grant usage, select on all sequences in schema delivery to authenticated;

grant select, insert, update on identity.person_notification_preference to authenticated;
grant select, insert, update on identity.web_push_subscription to authenticated;
grant select on delivery.notification_template, delivery.notification_dispatch to authenticated;

alter table identity.person_notification_preference enable row level security;
alter table identity.web_push_subscription enable row level security;
alter table delivery.notification_template enable row level security;
alter table delivery.notification_dispatch enable row level security;

